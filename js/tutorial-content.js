  getChapter(chapterId) {
    return this.chapters.find(c => c.id === chapterId) || null;
  },

  getStep(chapterId, stepIndex) {
    const ch = this.getChapter(chapterId);
    if (!ch || stepIndex < 0 || stepIndex >= ch.steps.length) return null;
    return ch.steps[stepIndex];
  },

  getTotalSteps() {
    return this.chapters.reduce((sum, ch) => sum + ch.steps.length, 0);
  },

  getChapterCount() {
    return this.chapters.length;
  },

  // 获取所有步骤的扁平列表（用于进度追踪）
  getAllStepsFlat() {
    const flat = [];
    this.chapters.forEach(ch => {
      ch.steps.forEach((step, i) => {
        flat.push({
          chapterId: ch.id,
          chapterTitle: ch.title,
          stepIndex: i,
          stepId: TutorialContent._stepId(ch.id, i),
          step: step
        });
      });
    });
    return flat;
  },

